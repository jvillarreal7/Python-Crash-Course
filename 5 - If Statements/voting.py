# Simple if statements
age = 17
if age >= 18:
	print("You're old enough to vote!")
	print("Have you registered to vote yet?")
	
# If-else statements
age = 17
if age >= 18:
	print("You're old enough to vote!")
	print("Have you registered to vote yet?")
else:
	print("Sorry, you're too young to vote.")
	print("Please register as soon as you're 18.")
