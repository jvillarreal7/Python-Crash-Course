# Sort a list permanently
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

# Reverse sorting
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)

# Sort a list temporarily
cars = ['bmw', 'audi', 'toyota', 'subaru']

print("Here's the original list:")
print(cars)

print("Here's the sorted list:")
print(sorted(cars))

print("Here's the original list again:")
print(cars)

# Print list in reverse order
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)
cars.reverse()
print(cars)

# Length of a list
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))
