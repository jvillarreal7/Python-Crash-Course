# Using the range() function
for value in range(1,6):
	print(value)

# Using range() to make a list of numbers
numbers = list(range(1,6))
print(numbers)

# Find the minimum, maximum, and sum of a list of numbers
digits = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]
print(min(digits))
print(max(digits))
print(sum(digits))
